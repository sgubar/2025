// main.c - главный файл программы
// Здесь происходит взаимодействие с пользователем

#include <stdio.h>  // для работы с вводом/выводом
#include "dk_tool.h"  // подключаем наши функции

int main() {
    char input[5];  // массив для хранения введенного числа (4 символа + 1 для нуля)
    
    // Запрашиваем у пользователя ввод числа
    printf("Введіть число (до 4 символів): ");
    
    // Читаем строку с клавиатуры
    // fgets автоматически добавляет нулевой символ в конец строки
    fgets(input, sizeof(input), stdin);
    
    // Проверяем корректность введенного числа
    if (!validate_input(input)) {
        // Если число некорректное, выводим сообщение об ошибке
        fprintf(stderr, "Помилка: Некоректний ввід\n");
        return 1;  // возвращаем код ошибки
    }
    
    // Преобразуем строку в число
    int number = str_to_num(input);
    
    // Выводим результат
    printf("Отримане число: %d\n", number);
    
    return 0;  // успешное завершение программы
}
